#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	size_t	len_s1;
	size_t	len_s2;

	len_s1 = ft_strlen(s1);
	len_s2 = ft_strlen(s2);
	result = malloc(len_s1 + len_s2 + 1);
	if (result)
	{
		ft_memmove(result, s1, len_s1);
		ft_memmove(result + len_s1, s2, len_s2);
		result[len_s1 + len_s2] = 0;
	}
	return (result);
}
